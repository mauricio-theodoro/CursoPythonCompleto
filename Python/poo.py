"""
Programação Orientada a Objetos - POO

- POO é um paradigma de programação que utiliza mapeamento
dos objetos do mundo real para modelos computacionais.

- Paradima de programação é a forma/metodologia utilizada para
pensar/desenvolver sistema.

Principais elementos da Orientação a Objetos
- Classe -> Modelo do objeto do mundo real sendo representado computacionalmente;
- Atributo -> Caracteristica do objeto.
- Método -> Comportamento do objeto (funções)
- Construtor -> Método especial utlizado para criar os objetos;
- Objeto -> Instância da classe.
"""
numero = 10

print(numero)
print(type(numero))

nome = 'Geek'

print(nome)
print(type(nome))

class Produto:
  pass

ps4 = Produto()
print(ps4)
print(type(ps4))
